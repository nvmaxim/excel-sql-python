# Исходные данные в виде многострочной строки
data = """manager;department;revenue;experience;period
Иван;А;15000;2;Q1
Мария;Б;22000;5;Q1
Петр;А;17000;3;Q1
Анна;В;12000;1;Q1
Сергей;Б;25000;4;Q1
Иван;А;18000;2;Q2
Мария;Б;19000;5;Q2
Анна;В;14000;1;Q2
Ольга;А;16000;2;Q2"""

# Разбиваем большую строку на отдельные строки по переносам
# strip() убирает лишние пробелы в начале и конце
# split('\n') разбивает строку на список строк там, где есть перенос
lines = data.strip().split("\n")
# Теперь lines = ['manager;department;revenue...', 'Иван;А;15000...', 'Мария;Б;22000...', ...]

# Получаем заголовки - берем первую строку и разбиваем ее по точкам с запятой
headers = lines[0].split(";")
# headers = ['manager', 'department', 'revenue', 'experience', 'period']

# Создаем список всех данных (кроме заголовка)
# lines[1:] означает "все строки начиная со второй (индекс 1) до конца"
# Для каждой строки разбиваем ее по точкам с запятой
rows = [line.split(";") for line in lines[1:]]
# rows = [['Иван', 'А', '15000', '2', 'Q1'], ['Мария', 'Б', '22000', '5', 'Q1'], ...]

# Создаем пустой словарь для хранения результатов
result = {}

# Для каждого заголовка создаем пустой список в словаре
for header in headers:
    result[header] = []
# Теперь result = {'manager': [], 'department': [], 'revenue': [], ...}

# Заполняем данные в словарь
# Проходим по каждой строке данных
for row in rows:
    # row = ['Иван', 'А', '15000', '2', 'Q1']
    # enumerate(row) дает нам пары: (0, 'Иван'), (1, 'А'), (2, '15000'), ...
    for i, value in enumerate(row):
        # i - номер столбца (0, 1, 2, 3, 4)
        # value - значение в этой ячейке
        # headers[i] - название столбца по этому номеру
        # Добавляем значение в соответствующий список
        result[headers[i]].append(value)

# Теперь result выглядит так:
# {
#   'manager': ['Иван', 'Мария', 'Петр', ...],
#   'department': ['А', 'Б', 'А', ...],
#   'revenue': ['15000', '22000', '17000', ...],
#   ...
# }

# Форматируем красивый вывод
print("{")  # Начало словаря

# Проходим по всем ключам (названиям столбцов)
for key in headers:
    # Если это текстовые поля - обрамляем значения кавычками
    if key in ["manager", "department", "period"]:
        # Создаем новый список, где каждое значение в кавычках
        formatted_values = [f'"{v}"' for v in result[key]]
        # formatted_values = ['"Иван"', '"Мария"', '"Петр"', ...]

        # Выводим строку: "key": [значение1, значение2, ...],
        print(f'    "{key}": [{", ".join(formatted_values)}],')
        # join объединяет список в строку через запятую: '"Иван", "Мария", "Петр"'

    else:
        # Для числовых полей оставляем значения как есть (без кавычек)
        print(f'    "{key}": [{", ".join(result[key])}],')
        # join объединяет список: '15000, 22000, 17000, ...'

print("}")  # Конец словаря
